<h1>Play 5</h1>

<p>5-a-side game mode for team play ranking</p>

<p>Play 5 is a 5v5 game mode where the player picks up to 4 other players and faces another team.</p>

<p>
  The player can select teammates from:
  <ul>
    <li>The team, if there are players.</li>
    <li>The player ranking, if the team doesn't have enough players.</li>
  </ul>
</p>

<p>The player can always pick a goalkeeper bot, in order to be able to balance a very offensive set of players.</p>

